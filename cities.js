// Build a { continent: { country: [cities...] } } tree for the menu
function buildContinentTree() {
  const tree = {};
  for (const name of CONTINENT_ORDER) tree[name] = {};
  for (const entry of CITIES) {
    const cont = tree[entry.continent] || (tree[entry.continent] = {});
    (cont[entry.country] || (cont[entry.country] = [])).push(entry);
  }
  return tree;
}
